# 子行业名 -> 看板统一标签(中药Ⅱ -> 中药)
SUB_NORMALIZE = {"中药Ⅱ": "中药"}


def main() -> None:
    sub: dict[str, str] = {}
    dup = 0
    for name, codes in BOARDS.items():
        for c in codes:
            c = c.strip()
            if not c:
                continue
            if c in sub and sub[c] != name:
                # 同一代码出现在两个二级板块(极少) -> 保留首次, 计数
                dup += 1
                continue
            sub[c] = name
    # 归一化子行业名
    norm = {c: SUB_NORMALIZE.get(s, s) for c, s in sub.items()}
    payload = {"updated": date.today().strftime("%Y-%m-%d"), "sub": norm}
    out = HERE / "sw_medical_static.json"
    out.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")
    # 统计
    from collections import Counter
    cnt = Counter(norm.values())
    print(f"[静态底表] 输出: {out}")
    print(f"[静态底表] 成分股总数(去重后): {len(norm)} 只 (跨板块重复 {dup} 次)")
    for k in ["化学制药","生物制品","医疗器械","医疗服务","中药","医药商业"]:
        print(f"  - {k}: {cnt.get(k,0)} 只")
    # A 股口径(6 位, 首位 0369, 非 92 开头)
    ashare = [c for c in norm if len(c)==6 and c.isdigit() and not c.startswith("92") and c[0] in "0369"]
    print(f"[静态底表] 其中 A 股(沪深, 排除 92 北交所/B股): {len(ashare)} 只")


if __name__ == "__main__":
    main()
